def clean_ANF(file_ANF):
    # file_ANF = 'ANF_16_multi.txt'
    anf = open(file_ANF, "r")
    ANF_st = []
    anf_str = ''
    for i in anf:
        anf_str += i.strip()

    ANF_yi_str = anf_str.lstrip('[').rstrip(']').split(',')
    for yi_str in ANF_yi_str:
        yi_list_m = yi_str.strip().split('+')
        yi_list = []
        for x_and_x in yi_list_m:
            yi_list.append(x_and_x.strip().split('*'))
        ANF_st.append(yi_list)
    return ANF_st


